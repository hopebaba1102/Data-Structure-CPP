// Code Example A-2: Example of using the getline function

#include <iostream>

using namespace std;

int main()
{
   const int maxNameLength = 40;
   char name[maxNameLength+1];
   cout << "Enter your first and last names: ";
   cin.getline(name, maxNameLength);
   cout << "Your name is " << name << endl;
}
